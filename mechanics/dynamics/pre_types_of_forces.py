def list_types_of_forces():
    print("Common types of forces:")
    print("- Gravity: F = mg (downward)")
    print("- Normal force: Perpendicular to surface")
    print("- Friction: Opposes motion")
    print("- Tension: Along a string or rope")
    print("- Applied force: Any external push or pull")
    print("- Spring force: F = -kx (Hooke's law)")

if __name__ == "__main__":
    list_types_of_forces()
